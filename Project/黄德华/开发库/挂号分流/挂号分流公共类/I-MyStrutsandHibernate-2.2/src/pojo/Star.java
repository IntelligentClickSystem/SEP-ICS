package pojo;

public class Star {
	
	private int attitude;
	private int effects;
	public int getAttitude() {
		return attitude;
	}
	public void setAttitude(int attitude) {
		this.attitude = attitude;
	}
	public int getEffects() {
		return effects;
	}
	public void setEffects(int effects) {
		this.effects = effects;
	}
	
	

}
